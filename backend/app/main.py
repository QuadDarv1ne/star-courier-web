@app.exception_handler(HTTPException)
async def http_exception_handler(request, exc):
    """Обработчик HTTP исключений"""
    logger.error(f"❌ HTTP Exception ({exc.status_code}): {exc.detail}")
    
    # Add more detailed error information
    error_details = {
        "error": exc.detail,
        "status_code": exc.status_code,
        "timestamp": datetime.now().isoformat(),
        "path": str(request.url),
        "method": request.method,
        "client": request.client.host if request.client else None
    }
    
    # Add request body for debugging in development
    if settings.debug and hasattr(request, 'body'):
        try:
            error_details["request_body"] = await request.body()
        except:
            pass
    
    return JSONResponse(
        status_code=exc.status_code,
        content=error_details
    )


@app.exception_handler(Exception)
async def general_exception_handler(request, exc):
    """Обработчик общих исключений"""
    logger.error(f"❌ Необработанное исключение: {str(exc)}", exc_info=True)
    
    # Create detailed error response
    error_details = {
        "error": "Internal server error",
        "status_code": 500,
        "timestamp": datetime.now().isoformat(),
        "path": str(request.url),
        "method": request.method,
        "client": request.client.host if request.client else None
    }
    
    # Add exception details in debug mode
    if settings.debug:
        error_details["details"] = str(exc)
        error_details["exception_type"] = type(exc).__name__
        
        # Try to get traceback
        import traceback
        error_details["traceback"] = traceback.format_exc()
    
    return JSONResponse(
        status_code=500,
        content=error_details
    )

# Rate limiting middleware
async def rate_limit_middleware(request: Request, call_next):
    # Get client IP
    client_ip = request.client.host if request.client else "unknown"
    
    # Get current time
    now = time.time()
    
    # Clean old requests (older than 1 minute)
    request_counts[client_ip] = [timestamp for timestamp in request_counts[client_ip] if now - timestamp < 60]
    
    # Check if limit exceeded (60 requests per minute)
    if len(request_counts[client_ip]) >= 60:
        return JSONResponse(
            status_code=429,
            content={"error": "Слишком много запросов. Пожалуйста, попробуйте позже."}
        )
    
    # Add current request
    request_counts[client_ip].append(now)
    
    # Continue with request
    response = await call_next(request)
    return response

# Apply rate limiting middleware
app.middleware("http")(rate_limit_middleware)

