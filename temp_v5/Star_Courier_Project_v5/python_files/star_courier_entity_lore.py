# === СВЯЗЬ С ИГРОВЫМ ПРОЦЕССОМ ===

def get_lore_entry(category, entry_id, game_state):
    """Получить запись лора, если условия выполнены"""
    if category == "entity":
        history = ENTITY_HISTORY.get(entry_id)
    elif category == "guardian":
        history = GUARDIAN_HISTORY.get(entry_id)
    elif category == "artifact":
        history = ANCIENT_ARTIFACTS.get(entry_id)
    elif category == "prophecy":
        history = PROPHECIES.get(entry_id)
    else:
        return None
    
    return history

def get_available_lore(game_state):
    """Получить доступные записи лора на основе прогресса"""
    available = []
    
    chapter = game_state.get("current_chapter", 1)
    knowledge = game_state.get("knowledge", 0)
    
    # Базовый лор открывается по главам
    lore_unlocks = {
        1: [("entity", "origin")],
        6: [("entity", "first_contact")],
        11: [("entity", "the_bargain"), ("prophecy", "observer_prophecy")],
        12: [("entity", "corruption"), ("artifact", "time_crystal")],
        14: [("entity", "the_catastrophe"), ("artifact", "anchor_station")],
        15: [("entity", "awakening")],
        17: [("guardian", "identity"), ("guardian", "before_guardianship")],
        18: [("entity", "the_sacrifice"), ("guardian", "current_purpose"), 
             ("prophecy", "original_prophecy"), ("artifact", "guardian_artifacts")]
    }
    
    for unlock_chapter, entries in lore_unlocks.items():
        if chapter >= unlock_chapter:
            for entry in entries:
                available.append(get_lore_entry(entry[0], entry[1], game_state))
    
    # Дополнительный лор за высокое знание
    if knowledge >= 50:
        available.append(get_lore_entry("entity", "the_sacrifice", game_state))
    
    return [a for a in available if a is not None]
