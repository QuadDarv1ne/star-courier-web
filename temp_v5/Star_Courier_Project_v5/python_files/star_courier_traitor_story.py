def get_traitor_dialogue(dialogue_id, game_state):
    """Получить диалог предателя"""
    return TRAITOR_DIALOGUES.get(dialogue_id)

def get_traitor_final_role(ending, traitor_status):
    """Получить роль предателя в финале"""
    ending_roles = TRAITOR_FINAL_ROLES.get(ending, {})
    return ending_roles.get(f"traitor_{traitor_status}")

def determine_traitor_appearance(chapter, game_state):
    """Определить, появляется ли предатель в главе"""
    # Предатель появляется в главе 15 и в финале (18)
    if chapter == 15:
        return "revelation"
    elif chapter == 18:
        traitor_status = game_state.get("traitor_status", "unknown")
        return traitor_status
    return None
